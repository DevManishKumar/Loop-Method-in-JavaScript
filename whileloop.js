
const whileloop = () => {
  // While Loop
  // It's similar to for loop in terms of complexity aand also very straight forward to use.

  const numbers = [11, 22, 33, 44, 55];

  let i = 0;
  while (i < numbers.length) {
    console.log(numbers[i]);
    i++;
  }

  // Result : 11 22 33 44 55
};

export default whileloop;
